#include <stdio.h>
#include <stdlib.h>

int main()
{
    int N;
    double m[10000];
    double sum = 0;
    double t;
    
    scanf("%d\n", &N);
    
    for(int i = 0; i < N; ++i){
        
        scanf("%lf", &m[i]);
        
    }
 
   for(int i = 0; i < N; ++i){
       
       sum = sum + m[i];
       
   }
    
   t = sum / N;
   
   printf ("%f", t);
    

    return 0;
}
